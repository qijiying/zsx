package com.zsx.fwmp.web.others.setting.bean;

public class Server {
	
	private String accessUrl;
	private String protocol;
	private String environment;
	private String postShare;
	private String newShare;
	private String workMatterShare;
	private String poliyShare;
	private String govermentShare;
	
	
	public String getEnvironment() {
		return environment;
	}
	
	public void setEnvironment(String environment) {
		this.environment = environment;
	}

	public String getProtocol() {
		return protocol;
	}
	
	public void setProtocol(String protocol) {
		this.protocol = protocol;
	}

	public String getAccessUrl() {
		return accessUrl;
	}

	public void setAccessUrl(String accessUrl) {
		this.accessUrl = accessUrl;
	}

	/**
	 * @return the postShare
	 */
	public String getPostShare() {
		return postShare;
	}

	/**
	 * @param postShare the postShare to set
	 */
	public void setPostShare(String postShare) {
		this.postShare = postShare;
	}

	/**
	 * @return the newShare
	 */
	public String getNewShare() {
		return newShare;
	}

	/**
	 * @param newShare the newShare to set
	 */
	public void setNewShare(String newShare) {
		this.newShare = newShare;
	}

	/**
	 * @return the workMatterShare
	 */
	public String getWorkMatterShare() {
		return workMatterShare;
	}

	/**
	 * @param workMatterShare the workMatterShare to set
	 */
	public void setWorkMatterShare(String workMatterShare) {
		this.workMatterShare = workMatterShare;
	}

	/**
	 * @return the poliyShare
	 */
	public String getPoliyShare() {
		return poliyShare;
	}

	/**
	 * @param poliyShare the poliyShare to set
	 */
	public void setPoliyShare(String poliyShare) {
		this.poliyShare = poliyShare;
	}

	/**
	 * @return the govermentShare
	 */
	public String getGovermentShare() {
		return govermentShare;
	}

	/**
	 * @param govermentShare the govermentShare to set
	 */
	public void setGovermentShare(String govermentShare) {
		this.govermentShare = govermentShare;
	}
	
	
}
